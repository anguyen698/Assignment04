//STEP 1
function halfNumber(num) {
  let result = num / 2;
  window.console.log(`Half of ${num} is ${result}`);
  return result;
}

//STEP 2
function squareNumber(num) {
  let result = num * num;
  window.console.log(`The result of squaring ${num} is ${result}`);
  return result;
}

//STEP 3
function percentOf(x, y) {
  let result = (x / y) * 100;
  window.console.log(`${x} is ${result}% of ${y}`);
  return result;
}

//STEP 4
function findModulus(x, y) {
  let result = x % y;
  window.console.log(`${result} is the modulus of ${x} and ${y}`);
  return result;
}

